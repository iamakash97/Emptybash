exports.calculate=function(a,b,c)
{
    var d=parseInt(a);
    var e=parseInt(b);
    var c=parseInt(c);

    var f=((d+e+c)/3);
    return f;
    
}
exports.multiplication=function(a,b,c)
{
    var d=parseInt(a);
    var e=parseInt(b);
    var c=parseInt(c);

    var f=(d*e*c);
    return f;
    
}